"use strict";
// If else
let tired = false;
let activity = tired ? "take rest" : "go for workout";
console.log(activity);
// switch case
let fruits = "apple";
switch (fruits) {
    case "banana":
        console.log("eat banana");
        break;
    case "apple":
        console.log("eat apple");
        break;
    case "orange":
        console.log("eat orange");
        break;
    default:
        console.log("no fruits left");
        break;
}
